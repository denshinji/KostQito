package com.example.frikos;

public class Kost {
    private String name;
    private String photo;
    private String harga;
    private String ukuran;
    private String alamat;
    private String kamarmandi;
    private String wifi;
    private String ac;

    String getName() {
        return name;
    }

    void setName(String name) {
        this.name = name;
    }

    String getPhoto() {
        return photo;
    }

    void setPhoto(String photo) {
        this.photo = photo;
    }

    String getHarga() {
        return harga;
    }

    void setHarga(String harga) {
        this.harga = harga;
    }
    String getUkuran() {
        return ukuran;
    }

    void setUkuran(String ukuran) {
        this.ukuran = ukuran;
    }

    String getAlamat() {
        return alamat;
    }

    void setAlamat(String alamat) {
        this.alamat = alamat;
    }

    String getKamarmandi() {
        return kamarmandi;
    }

    void setKamarmandi(String kamarmandi) {
        this.kamarmandi = kamarmandi;
    }

    String getWifi() {
        return wifi;
    }

    void setWifi(String wifi) {
        this.wifi = wifi;
    }

    String getAc() {
        return ac;
    }

    void setAc(String ac) {
        this.ac = ac;
    }
}
